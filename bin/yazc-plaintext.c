static void generate_bits_15_2_key2(unsigned char key3, unsigned short *value)
{
   unsigned char key3tmp;
   unsigned int i = 0x10000;
   unsigned int valuei = 0;
   do {
      key3tmp = ((i | 2) * ((i | 3))) >> 8;
      if (key3 == key3tmp)
      {
         value[valuei] = i;
         ++valuei;
      }
   } while (i -= 4);
}

static void complete_bits_1_0_key2i_plus_1(const struct key2_table *key2i,
                                           struct key2_table *key2i_plus_1)
{
   const unsigned int key2i_size = key2i->size;
   for (unsigned int i = 0; i < key2i_size; ++i)
   {
      unsigned char key2i_plus_1_msb = key2i_plus_1->array[i] >> 24;

      /* complete bits [1..0] of key2i+1 from bits [9..8] */
      unsigned int tmp = key2i->array[i] ^ crc_32_invtab[key2i_plus_1_msb];
      tmp = (tmp >> 8) & 0x3;   /* keep only bit 9 and 8 */
      key2i_plus_1->array[i] |= tmp; /* add them up to key2i+1 */
   }
}

static void generate_key2i_table_from_key2i_plus_1(struct key2_table *key2i,
                                                   const struct key2_table *key2i_plus_1,
                                                   unsigned char key3)
{
   unsigned int key2i_index = 0;
   unsigned short key2i_14bits_lsb[64];
   const unsigned int key2i_plus_1_size = key2i_plus_1->size;

   /* generate the 64 possible values for the [15..2] bits of key2i */
   generate_bits_15_2_key2(key3, key2i_14bits_lsb);

   /* for each ~2^22 possible values of bits [31..2] of key2i+1 */
   for (unsigned int i = 0; i < key2i_plus_1_size; ++i)
   {
      unsigned int key2itmp;
      key2itmp = (KEY2(key2i_plus_1, i) << 8) ^ crc_32_invtab[KEY2(key2i_plus_1, i) >> 24];
      for (int j = 0; j < 64; ++j)
      {
         unsigned int bits10_15_rhs = key2itmp & 0xfc00;
         unsigned int bits10_15_lhs = (unsigned int)key2i_14bits_lsb[j] & 0xfc00;

         /* the left and right hand side share the same 6 bits in
            position [15..10] */
         if (bits10_15_rhs == bits10_15_lhs)
         {
            if (key2i_index >= key2i->size)
               realloc_key2_table(key2i, key2i->size + 1);

            /* save bits [31..8] */
            key2i->array[key2i_index] = key2itmp & 0xffffff00;

            /* save bits [7..2] */
            key2i->array[key2i_index] |= key2i_14bits_lsb[j];

            ++key2i_index;
         }
      }
   }
}

